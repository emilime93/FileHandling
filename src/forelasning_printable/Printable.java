package forelasning_printable;

/**
 * An <code>Interface</code> that wows to be able to generate a printable representation of whomever implements it.
 */
public interface Printable {
    /**
     * Generates a printable <code>String</code> representation of the object.
     * @return The representation of the object.
     */
    String generatePrintableRepresentation();
}
